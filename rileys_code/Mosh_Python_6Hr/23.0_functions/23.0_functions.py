# Function definition, follows the format def function_name(parameters go here):
# Functions must be called below the definition
# Indentation dictates what is run in the function
# Best practice is to have 2 blank lines after a function definition
def greet_user():
    # These next 2 lines are run when the function is called
    print("Hi there!")
    print("Welcome aboard")


# Print start and finish, and call the function in between
print("Start")
greet_user()
print("Finish")